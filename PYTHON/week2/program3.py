# using comma and concatenation with strings

score1 = '39'
score2 = '25'
print() # printing nothing within the brackets makes a space
print(score1+score2) # adding 2 string numbers will combine the numbers together into 1 string
print(score1,score2) # using a comma makes a space between the 2 string values

print(9 + 3) # + is the addition operator
print('9' + '13') # + is the concatenation operator

# print('9' + 3) cannot add a string to a number
print(int('9') + 13) # changing the string to an int is called explicit type casting or conversion

