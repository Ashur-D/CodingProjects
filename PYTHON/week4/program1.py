# using escape sequences in python
print("Hello world this is my python new line example\nhere is a new line \nand another one\n")

print("Yesterday, I finished reading \"the merchant of Venice\" novel!") # if you want to quote something you need to esc with \ as you started the print with double quote

print('Yesterday, I finished reading "the merchant of Venice" novel!') # unlike above we are using 2 diffrent types of quotes so we dont need to esc

print('hello\tpython class!') # will add a tab

